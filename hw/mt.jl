md"""
## 2. Definitely differentiable

Complete the following routine to compute the derivative of the packed LU factorization of $A$ in the direction $\delta A$ when $A$ is strictly column diagonally dominant

    deriv_sddlu(A, δA)

As a hint: Try working with $M = L^{-1} (\delta A) U^{-1}$.

You may use the following code to compute a centered finite difference estimate for comparison.
"""

# ╔═╡ df58ad7a-b3cc-49f3-91ad-941067529c38
function fd_sddlu(A, δA; h=1e-8)
	Ap = A + h*δA
	Am = A - h*δA
	lu!(Ap)
	lu!(Am)
	(Ap-Am)/(2*h)
end

