## 3. Dratted determinant

Suppose $A$ is well-conditioned and `F = lu(A)` is computed, and consider

$M = \begin{bmatrix} A & b \\ c^T & d \end{bmatrix}$

Write an $O(n^2)$ algorithm to compute the determinant of $M$.  Your code should have the calling signature

    result = detM(F, b, c, d)

*Note*: You may call `det(F)` for the computed LU factorization of $A$ -- that is an $O(n)$ algorithm.
