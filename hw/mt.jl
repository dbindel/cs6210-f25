# ╔═╡ e4964d01-791c-4dde-bf26-e33469168f60
# Reference computation -- this is cubic time
function detMref(F, b, c, d)
	A = zeros(size(F)...)
	A[F.p,:] = F.L * F.U
	M = [A b; c' d]
	det(M)
end

